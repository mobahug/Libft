#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*ptr;

	ptr = (char *)malloc(sizeof(*ptr) * size);
	if (ptr == NULL)
		return (NULL);
	ft_strclr(ptr);
	return (0);
}
