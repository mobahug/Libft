#include "libft.h"

void	ft_putstr(char const *s)
{
	char	*s_s;
	int		i;

	s_s = (char *)s;
	i = 0;
	while (s_s[i] != '\0')
	{
		write(1, &s_s[i], 1);
		i++;
	}
}
